package veriable;

public class Var7 {
    public static void main(String[] args) {
        //리터럴
        //코드에서 개발자가 직접 적은 100, 10.5, true, A, Hello Java와 같은 고정된 값을
        //프래그래밍 용어로 리터럴(literal)이라 한다.


        int a =100; //정수 리터럴
        double b = 10.5; //실수 리터럴
        boolean c = true; //불리언(boolean) true, false 입력 가능, 불리언 리터럴
        char d = 'A'; //문자 하나만 가능, 문자 하나 리터럴
        String e = "Hello Java"; //문자열, 문자열을 다루기 위한 특별한 타입, 문자열 리터럴

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);



    }
}
